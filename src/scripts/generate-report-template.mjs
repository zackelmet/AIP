/**
 * Generates the external-report-template.docx used by the report engine.
 * Uses the `docx` npm package to build a proper OOXML document that
 * Docxtemplater can parse. Placeholders are wrapped in {{double braces}}
 * matching the delimiters set in docx-template.ts.
 *
 * Run: node scripts/generate-report-template.mjs
 */

import {
  Document,
  Packer,
  Paragraph,
  TextRun,
  HeadingLevel,
  AlignmentType,
  BorderStyle,
  ShadingType,
  TableRow,
  TableCell,
  Table,
  WidthType,
  PageBreak,
} from "docx";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const outDir = path.join(__dirname, "..", "public", "templates");
fs.mkdirSync(outDir, { recursive: true });

/* ── helpers ─────────────────────────────────────────────────── */
const ph = (tag) => new TextRun({ text: `{{${tag}}}` });
const bold = (text) => new TextRun({ text, bold: true });
const normal = (text) => new TextRun({ text });

const h1 = (text) =>
  new Paragraph({ heading: HeadingLevel.HEADING_1, children: [new TextRun({ text, bold: true })] });

const h2 = (text) =>
  new Paragraph({ heading: HeadingLevel.HEADING_2, children: [new TextRun({ text, bold: true })] });

const h3 = (text) =>
  new Paragraph({ heading: HeadingLevel.HEADING_3, children: [new TextRun({ text, bold: true })] });

const p = (...runs) => new Paragraph({ children: runs });

const label = (labelText, placeholder) =>
  new Paragraph({
    children: [
      new TextRun({ text: `${labelText}: `, bold: true }),
      ph(placeholder),
    ],
  });

const pageBreak = () =>
  new Paragraph({ children: [new PageBreak()] });

/* ── document sections ────────────────────────────────────────── */
const children = [
  // ── COVER PAGE ──────────────────────────────────────────────
  new Paragraph({
    alignment: AlignmentType.CENTER,
    children: [new TextRun({ text: "PENETRATION TEST REPORT", bold: true, size: 48 })],
  }),
  new Paragraph({ alignment: AlignmentType.CENTER, children: [ph("REPORT_TITLE")] }),
  new Paragraph({ alignment: AlignmentType.CENTER, children: [ph("CLIENT_NAME")] }),
  new Paragraph({ alignment: AlignmentType.CENTER, children: [ph("COMPLETION_DATE")] }),
  pageBreak(),

  // ── METADATA TABLE ──────────────────────────────────────────
  h1("Report Details"),
  new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows: [
      new TableRow({ children: [
        new TableCell({ children: [p(bold("Report Version"))] }),
        new TableCell({ children: [p(ph("REPORT_VERSION"))] }),
      ]}),
      new TableRow({ children: [
        new TableCell({ children: [p(bold("Report Date"))] }),
        new TableCell({ children: [p(ph("REPORT_DATE"))] }),
      ]}),
      new TableRow({ children: [
        new TableCell({ children: [p(bold("Prepared By"))] }),
        new TableCell({ children: [p(ph("TESTER_NAME"))] }),
      ]}),
      new TableRow({ children: [
        new TableCell({ children: [p(bold("Target"))] }),
        new TableCell({ children: [p(ph("TARGET"))] }),
      ]}),
      new TableRow({ children: [
        new TableCell({ children: [p(bold("Notes"))] }),
        new TableCell({ children: [p(ph("REPORT_NOTES"))] }),
      ]}),
    ],
  }),
  pageBreak(),

  // ── TABLE OF CONTENTS ────────────────────────────────────────
  h1("Table of Contents"),
  p(normal("Findings:")),
  // Loop tag for TOC
  p(ph("#toc_findings")),
  p(ph("toc_index"), normal(". "), ph("toc_title")),
  p(ph("/toc_findings")),
  pageBreak(),

  // ── EXECUTIVE SUMMARY ────────────────────────────────────────
  h1("Executive Summary"),
  p(ph("EXECUTIVE_SUMMARY_PARAGRAPH_1")),
  p(ph("EXECUTIVE_SUMMARY_PARAGRAPH_2")),
  pageBreak(),

  // ── PURPOSE ──────────────────────────────────────────────────
  h1("Assessment Overview"),
  h2("Purpose"),
  p(ph("PURPOSE_STATEMENT")),
  h2("Scope"),
  p(ph("SCOPE_TARGET")),
  pageBreak(),

  // ── FINDINGS ─────────────────────────────────────────────────
  h1("Findings"),
  // Docxtemplater loop — each finding is a separate section
  p(ph("#findings")),
  h2("Finding {{index}}: {{title}}"),
  label("Severity", "severity"),
  label("CVSS Score", "cvss_vector"),
  h3("Description"),
  p(ph("description")),
  h3("Proof of Concept"),
  p(ph("poc")),
  h3("Impact"),
  p(ph("impact")),
  h3("Remediation"),
  p(ph("remediation")),
  p(ph("/findings")),
  pageBreak(),

  // ── APPENDIX ─────────────────────────────────────────────────
  h1("Appendix"),
  p(normal("This report was generated by the Affordable Pentesting platform.")),
];

/* ── build & write ────────────────────────────────────────────── */
const doc = new Document({
  sections: [{ children }],
});

const outPath = path.join(outDir, "external-report-template.docx");
const buffer = await Packer.toBuffer(doc);
fs.writeFileSync(outPath, buffer);
console.log(`✅  Template written to ${outPath} (${(buffer.length / 1024).toFixed(1)} KB)`);

// Also write as webapp template (same base template)
const webappPath = path.join(outDir, "webapp-report-template.docx");
fs.copyFileSync(outPath, webappPath);
console.log(`✅  Webapp template copied to ${webappPath}`);
